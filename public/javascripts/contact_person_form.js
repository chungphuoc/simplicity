function toggleCategoryMode() {
	toggleVisibility('new_cp_panel'); 
	emt = document.getElementById("add_category");
	if ( emt.value == 0) {
		emt.value = 1;
	} else {
		emt.value = 0;
	}
	popup = document.getElementById("contact_person_cp_category_id")
	popup.disabled = !popup.disabled;
}

function showCategoryMode() {
	emt = document.getElementById("add_category");
	alert( emt.value );
}
